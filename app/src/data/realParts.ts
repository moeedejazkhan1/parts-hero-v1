// Parts organized by category for tree navigation
export const partsByCategory: Record<string, Record<string, Record<string, RealPart[]>>> = {};

realParts.forEach(part => {
  if (!partsByCategory[part.category]) partsByCategory[part.category] = {};
  if (!partsByCategory[part.category][part.subcategory]) partsByCategory[part.category][part.subcategory] = {};
  if (!partsByCategory[part.category][part.subcategory][part.component]) partsByCategory[part.category][part.subcategory][part.component] = [];
  partsByCategory[part.category][part.subcategory][part.component].push(part);
});

// Get related "Don't Forget" parts
export function getRelatedParts(part: RealPart): RealPart[] {
  return part.relatedParts
    .map(id => realParts.find(p => p.id === id))
    .filter(Boolean) as RealPart[];
}
